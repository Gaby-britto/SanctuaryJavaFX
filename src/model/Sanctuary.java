package model;

public class Sanctuary {
	
	private int id;
	private String localSanctuary;
	private String dataSanctuary;
	private String horarioSanctuary;
	private int valorSanctuary;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getLocalSanctuary() {
		return localSanctuary;
	}
	public void setLocalSanctuary(String localSanctuary) {
		this.localSanctuary = localSanctuary;
	}
	public String getDataSanctuary() {
		return dataSanctuary;
	}
	public void setDataSanctuary(String dataSanctuary) {
		this.dataSanctuary = dataSanctuary;
	}
	public String getHorarioSanctuary() {
		return horarioSanctuary;
	}
	public void setHorarioSanctuary(String horarioSanctuary) {
		this.horarioSanctuary = horarioSanctuary;
	}
	public int getValorSanctuary() {
		return valorSanctuary;
	}
	public void setValorSanctuary(int valorSanctuary) {
		this.valorSanctuary = valorSanctuary;
	}
}
